# If-else example
age = 18
if age >= 18:
    print("You are an adult")
else:
    print("You are a minor")

# For loop example
print("\nCounting to 5:")
for i in range(1, 6):
    print(i)

# While loop example
print("\nCountdown from 5:")
count = 5
while count > 0:
    print(count)
    count -= 1




